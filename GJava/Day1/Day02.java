 
public class Day02 {
    public static void main(String[] args){
        // declare integer variable
        int age = 21;
        // declare floting-point variable
        double height = 5.8;
        // declare string variable
        String name = "Mohd Anas";
        // declare float variable
        float weight = 150.5f; // The 'f' or 'F' is important for floats.
        // declare char 
        char favChar = 'L';
        // Boolean
        boolean isChaiLover = true;

        System.out.println("Name: "+name);
        System.out.println("Age: " +age);
        System.out.println("Height: "+height);
        System.out.println("Weight: "+weight);
        System.out.println("Fav Charachter: "+favChar);
        System.out.println("Are you Chai lover: "+isChaiLover);
    }
}


